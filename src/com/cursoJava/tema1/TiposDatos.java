package com.cursoJava.tema1;

public class TiposDatos {

    //Primitivos. no pueden ser nulos
    //Enteros
    byte num1 = 1;
    short num2 = 2;
    int num3 = 3;
    long num4 = 4L;

    //Flotante
    float num5 = 2.4f;
    double num6 = 5.6666d;

    //Caracter
    char char1 = 'a';

    //Boolean
    boolean verdadero = true;
    boolean falso = false;

    //Cadenas de texto
    String nombre = "Edgar";
    String apellido = "Alfonso";

    //Tipos Wrapper. envuelven los tipos primitivos para tratarlos como objetos
    Integer numero = null;
    Long numero2 = 2L;



}
